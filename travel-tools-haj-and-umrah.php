         <h3>The Haj</h3><br>
         <p>
          In the forenoon of the eighth day of Dhul-Hijja, a pilgrim purifies himself once again by bathing as he did before Umrah in the place in which he is staying, if convenient. He puts on his Ihram and says: “Here I am for Haj. Here I am, oh Allah, here I am. Here I am. You have no partner. Here I am. Surely all praise, grace and dominion is yours, and you have no partners.” If he fears that something will prevent him from completing his Haj he should make a condition when he makes his intentions, saying: “If I am prevented by any obstacle my place is wherever I am held up.” If he has no such fear, he doesn’t make this condition.
        </p>
        <p>A pilgrim goes to Mina and there prays Dhuhr, Asr, Magrib, Isha and Fajr, shortening his four unit prayers so as to make them two units each, without combining them. When the sun rises, he goes to Arafah and there prays Dhuhr and Asr combined at the time of Dhuhr, making each one two units. He remains in Namira Mosque until sunset if possible. He remembers Allah and makes as many supplications as possible while facing the Qibla.</p>
