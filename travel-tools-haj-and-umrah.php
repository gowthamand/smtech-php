        <h3>The Umrah</h3><br>
        <p>If a pilgrim wishes to be ritually pure for Umrah, he should shed his clothing and bathe as he would after sexual defilement, if convenient. He should perfume his head and beard with the best oil he can find. There is no harm in what remains of it after Ihram.</p>
        <p>Bathing for Ihram is Sunnah for both men and women, including menstruating women and those experiencing postnatal bleeding. After bathing and preparing himself, a. pilgrim, other than those menstruating or experiencing postnatal bleeding, prays the obligatory prayer, if it is time. Otherwise, he makes his intention by praying the two Sunnah Rakass which are made each time Wudhu is performed.</p>
        <p>When he finishes his prayer he should say: “Here I am for Umrah, here I am, and Oh Allah, here I am. Here I am. You have no partner. Here I am. Surely all praise, grace and dominion is yours, and you have no partner.” [Talbeeyah]. A man raises his voice when saying this and a woman says it so that only one beside her may hear her.</p>
        <p>One in Ihram should say the Talbeeyah as often as possible, especially when times and places change. For example: when descending or ascending during travel or when day or night approach. He should also ask Allah for His pleasure, for Heaven and seek refuge in Allah’s mercy from Hellfire.</p>
